package com.hamilton.seconds;

public class ComparisonOperators {

    public static void main(String[] args) {
        // Comparison Operators or Boolean Expressions
        int lillyAge = 9;
        int trainAge = 8;
        System.out.println(lillyAge > trainAge);
        System.out.println(lillyAge >= trainAge);
        System.out.println(lillyAge < trainAge);
        System.out.println(lillyAge <= trainAge);
        System.out.println(lillyAge == trainAge);
        System.out.println(lillyAge != trainAge);

    }


}
